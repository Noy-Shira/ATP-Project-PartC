package org.example.atpprojectpartc.Model;
/**
 * Enumeration representing all possible movement directions for the character within the maze.
 * Includes standard orthogonal movements (UP, DOWN, LEFT, RIGHT) and
 * diagonal movements (UP_LEFT, UP_RIGHT, DOWN_LEFT, DOWN_RIGHT).
 */
public enum Direction {
    UP, DOWN, LEFT, RIGHT,
    UP_LEFT, UP_RIGHT, DOWN_LEFT, DOWN_RIGHT
}